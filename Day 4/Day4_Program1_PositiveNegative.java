class Day4_Program1_PositiveNegative {
    public static void main(String[] args) {
        int number = -12;

        if (number > 0) {
            System.out.println(number + " is Positive");
        } else if (number < 0) {
            System.out.println(number + " is Negative");
        } else {
            System.out.println("Number is Zero");
        }
    }
}

